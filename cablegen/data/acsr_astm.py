def _build_entries() -> list[CableEntry]:
  entries = []
  for r in _RAW:
    entries.append(CableEntry(
      code_word=r[0], alt_code=r[1], size_awg_kcmil=r[2], stranding=r[3],
      area_al_mm2=r[4], area_steel_mm2=r[5],
      n_wires_al=r[6], wire_diam_al_mm=r[7],
      n_wires_steel=r[8], wire_diam_steel_mm=r[9],
      conductor_diameter_mm=r[10],
      mass_al_kg_km=r[11], mass_steel_kg_km=r[12], mass_total_kg_km=r[13],
      rated_strength_class_a_kn=r[14], rated_strength_class_b_kn=r[15],
      dc_resist_20c_ohm_km=r[16],
      source="ABNT NBR 7270",
    ))
  for r in _INCOMPLETE:
    entries.append(CableEntry(
      code_word=r[0], alt_code=r[1] or None,
      size_awg_kcmil=r[2], stranding=r[3],
      source="Aluminum Association",
    ))
  # Ordenar: entradas com code_word primeiro (alfabético), depois sem
  entries.sort(key=lambda e: (e.code_word == "", e.code_word.upper()))
  return entries


def get_family() -> CableFamily:
  return CableFamily(
    name="ACSR",
    description="Aluminum Conductor, Steel Reinforced",
    description_pt="CAA - Cabos de alumínio com alma de aço",
    output_filename="ACSR.md",
    columns=COLUMNS,
    entries=_build_entries(),
    references=[
      "[1] THE ALUMINUM ASSOCIATION. Code words for overhead aluminum electrical conductors, 7th edition, 1999.",
      "[2] ABNT NBR 7270. Cabos de alumínio nus com alma de aço zincado para linhas aéreas — Especificação, Segunda edição, Versão corrigida 2, 2010.",
    ],
    standard="ASTM B232 / ABNT NBR 7270",
  )
